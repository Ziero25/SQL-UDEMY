/* MAX - TRAZ O VALOR MÁXIMO DE UMA COLUNA */

SELECT MAX(FEVEREIRO) AS MAIOR_FEV
FROM VENDEDORES;

/* MIN - TRAZ O VALOR MÍNIMO DE UMA COLUNA */

SELECT MIN(FEVEREIRO) AS MENOR_FEV
FROM VENDEDORES;

/* AVG - TRAZ O VALOR MÉDIO DE UMA COLUNA */

SELECT AVG(FEVEREIRO) AS MEDIA_FEV
FROM VENDEDORES;

/* VARIAS FUNCOES */

SELECT MAX(JANEIRO) AS MAX_JAN,
	   MIN(JANEIRO) AS MIN_JAN,
	   AVG(JANEIRO) AS MED_JAN
FROM VENDEDORES;

/* TRUNCATE */
SELECT TRUNCATE(MAX(JANEIRO),2) AS MAX_JAN,
	   TRUNCATE(MIN(JANEIRO),2) AS MIN_JAN,
	   TRUNCATE(AVG(JANEIRO),2) AS MED_JAN
FROM VENDEDORES;

/* AGRAGANDO COM SUM() */
SELECT SUM(JANEIRO) AS TOTAL_JAN
FROM VENDEDORES;

SELECT TRUNCATE(SUM(JANEIRO),2) AS TOTAL_JAN,
	   TRUNCATE(SUM(FEVEREIRO),2) AS TOTAL_FEV,
	   TRUNCATE(SUM(MARCO),2) AS TOTAL_MAR
FROM VENDEDORES;

/* VENDAS POR SEXO */
 
SELECT SEXO, SUM(MARCO) AS TOTAL_MARCO
FROM VENDEDORES
GROUP BY SEXO;